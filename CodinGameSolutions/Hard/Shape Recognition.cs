using System;
using System.Collections.Generic;
using System.Linq;

class Program
{
    static void Main()
    {
        List<float[]> X = new List<float[]>();
        List<int> Y = new List<int>();
        foreach (var (ascii, label) in Training.Data)
        {
            int lbl = Array.IndexOf(Training.Names, label.Trim());
            if (lbl == -1) throw new Exception("Label not found: " + label);
            X.Add(Training.ConvertAsciiToInput(ascii));
            Y.Add(lbl);
        }

        for (int i = 0; i < Training.Names.Length; i++)
            Console.Error.WriteLine($"{Training.Names[i]}: {Y.Count(y => y == i)}");

        int inputSize = X[0].Length, hiddenSize = 32, outputSize = Training.Names.Length;
        var nn = new NeuralNet(inputSize, hiddenSize, outputSize);
        nn.Train(X, Y, epochs: 32, lr: 0.05f);

        int correct = 0;
        for (int i = 0; i < X.Count; i++)
        {
            int pred = nn.Predict(X[i]);
            Console.Error.WriteLine($"Pred: {Training.Names[pred]}, Actual: {Training.Names[Y[i]]}");
            if (pred == Y[i]) correct++;
        }
        Console.Error.WriteLine($"Final train accuracy: {(float)correct / X.Count:P1}");

        string inputAscii = "";
        for (int i = 0; i < 20; i++)
            inputAscii += Console.ReadLine() + "\n";
        float[] testInput = Training.ConvertAsciiToInput(inputAscii);
        int predicted = nn.Predict(testInput);
        Console.WriteLine(Training.Names[predicted]);
    }
}

class NeuralNet
{
    int inputSize, hiddenSize, outputSize;
    float[] wh, bh, wo, bo;
    Random rnd = new Random(42);

    public NeuralNet(int input, int hidden, int output)
    {
        inputSize = input; hiddenSize = hidden; outputSize = output;
        wh = new float[input * hidden];
        bh = new float[hidden];
        wo = new float[hidden * output];
        bo = new float[output];
        for (int i = 0; i < wh.Length; i++) wh[i] = ((float)rnd.NextDouble() - 0.5f) * 0.2f;
        for (int i = 0; i < wo.Length; i++) wo[i] = ((float)rnd.NextDouble() - 0.5f) * 0.2f;
    }
    public string ToBase64String()
    {
        var allParameters = wh.Concat(bh).Concat(wo).Concat(bo).ToArray();
        byte[] byteArray = new byte[allParameters.Length * sizeof(float)];
        Buffer.BlockCopy(allParameters, 0, byteArray, 0, byteArray.Length);
        string asciiWeights = Convert.ToBase64String(byteArray);
        return asciiWeights;
    }

    public string ToUltraCompressedString()
    {
        var all = wh.Concat(bh).Concat(wo).Concat(bo).ToArray();
        int N = all.Length;
        char[] chars = new char[(N + 1) / 2];
        for (int i = 0; i < N; i += 2)
        {
            byte b1 = (byte)Math.Round((Math.Max(-1, Math.Min(1, all[i])) + 1f) * 127.5f);
            byte b2 = (i + 1 < N) ? (byte)Math.Round((Math.Max(-1, Math.Min(1, all[i + 1])) + 1f) * 127.5f) : (byte)0;
            chars[i / 2] = (char)((b1 << 8) | b2);
        }
        return new string(chars);
    }

    public string ToCompressedString()
    {
        var all = wh.Concat(bh).Concat(wo).Concat(bo).ToArray();
        char[] chars = new char[all.Length];
        for (int i = 0; i < all.Length; i++)
        {
            float x = Math.Max(-1f, Math.Min(1f, all[i])); // Clamp for safety
            chars[i] = (char)((ushort)Math.Round((x + 1f) * 32767.5f));
        }
        return new string(chars);
    }

    static float Sigmoid(float x) => 1f / (1f + (float)Math.Exp(-x));
    static float SigmoidD(float x) { float s = Sigmoid(x); return s * (1 - s); }

    public int Predict(float[] input)
    {
        var (h, o) = Forward(input);
        var probs = Softmax(o);
        int maxi = 0; float maxv = probs[0];
        for (int i = 1; i < probs.Length; i++)
            if (probs[i] > maxv) { maxv = probs[i]; maxi = i; }
        return maxi;
    }

    public (float[] h, float[] o, float[] preh) ForwardFull(float[] input)
    {
        float[] preh = new float[hiddenSize];
        float[] h = new float[hiddenSize];
        for (int j = 0; j < hiddenSize; j++)
        {
            float sum = bh[j];
            for (int i = 0; i < inputSize; i++)
                sum += input[i] * wh[i * hiddenSize + j];
            preh[j] = sum;
            h[j] = Sigmoid(sum);
        }
        float[] o = new float[outputSize];
        for (int k = 0; k < outputSize; k++)
        {
            float sum = bo[k];
            for (int j = 0; j < hiddenSize; j++)
                sum += h[j] * wo[j * outputSize + k];
            o[k] = sum;
        }
        return (h, o, preh);
    }

    public (float[] h, float[] o) Forward(float[] input)
    {
        float[] h = new float[hiddenSize];
        for (int j = 0; j < hiddenSize; j++)
        {
            float sum = bh[j];
            for (int i = 0; i < inputSize; i++)
                sum += input[i] * wh[i * hiddenSize + j];
            h[j] = Sigmoid(sum);
        }
        float[] o = new float[outputSize];
        for (int k = 0; k < outputSize; k++)
        {
            float sum = bo[k];
            for (int j = 0; j < hiddenSize; j++)
                sum += h[j] * wo[j * outputSize + k];
            o[k] = sum;
        }
        return (h, o);
    }

    static float[] Softmax(float[] o)
    {
        float max = o.Max();
        float[] exp = new float[o.Length];
        float sum = 0;
        for (int i = 0; i < o.Length; i++) { exp[i] = (float)Math.Exp(o[i] - max); sum += exp[i]; }
        for (int i = 0; i < o.Length; i++) exp[i] /= sum;
        return exp;
    }

    public void Train(List<float[]> X, List<int> Y, int epochs = 100, float lr = 0.05f)
    {
        for (int epoch = 1; epoch <= epochs; epoch++)
        {
            int correct = 0;
            for (int n = 0; n < X.Count; n++)
            {
                float[] input = X[n];
                int label = Y[n];
                float[] h, o, preh;
                (h, o, preh) = ForwardFull(input);
                float[] probs = Softmax(o);
                if (Array.IndexOf(probs, probs.Max()) == label) correct++;
                float[] gradO = new float[outputSize];
                for (int k = 0; k < outputSize; k++)
                    gradO[k] = probs[k] - (k == label ? 1 : 0);
                float[] gradH = new float[hiddenSize];
                for (int j = 0; j < hiddenSize; j++)
                {
                    for (int k = 0; k < outputSize; k++)
                        gradH[j] += gradO[k] * wo[j * outputSize + k];
                    gradH[j] *= SigmoidD(preh[j]);
                }
                for (int k = 0; k < outputSize; k++)
                {
                    bo[k] -= lr * gradO[k];
                    for (int j = 0; j < hiddenSize; j++)
                        wo[j * outputSize + k] -= lr * gradO[k] * h[j];
                }
                for (int j = 0; j < hiddenSize; j++)
                {
                    bh[j] -= lr * gradH[j];
                    for (int i = 0; i < inputSize; i++)
                        wh[i * hiddenSize + j] -= lr * gradH[j] * input[i];
                }
            }
            if (epoch % 10 == 0 || epoch <= 10)
                Console.Error.WriteLine($"Epoch {epoch}: train acc = {(float)correct / X.Count:P1}");
        }
    }
  
}

